# handlers/help.py
from aiogram import Router, types, F
from utils.locale import rtl

router = Router()

FAQ = [
    ("چطور اتصال بگیرم؟", "اپ کلاینت پیشنهادی را نصب کنید و کانفیگ را وارد کنید. (راهنمای کامل به‌زودی اضافه می‌شود)"),
    ("سرعتم کند شد، چکار کنم؟", "برنامه را ببند/باز کن، سرور را عوض کن، اینترنت پایه را چک کن، و از وای‌فای ۵GHz استفاده کن."),
    ("چند دستگاه می‌توانم وصل کنم؟", "طبق پلن انتخابی. اگر بیشتر لازم دارید، پلن بالاتر یا کاستوم انتخاب کنید."),
    ("حجم تمام شد/رو به اتمام است؟", "می‌توانید پلن خود را تمدید یا پلن بزرگ‌تر تهیه کنید."),
    ("پروتکل‌ها کدام‌اند؟", "WireGuard، Hysteria2، VLESS/Reality. (در پلن کاستوم امکان انتخاب چندگانه هست)"),
    ("بازگشت وجه؟", "در صورت مشکل از سمت ما و عدم رفع، مطابق قوانین تا ۴۸ ساعت امکان بازگشت وجود دارد."),
]

@router.message(F.text == "📚 آموزش")
async def help_handler(m: types.Message):
    lines = [f"▪️ {q}\n{a}" for q, a in FAQ]
    await m.answer(rtl("❓ پرسش‌های پرتکرار:\n\n" + "\n\n".join(lines)))
